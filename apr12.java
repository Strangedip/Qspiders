public class apr12 {

// TERNARY OPERATORS
    // Conditional Operator
    // condition ? if true : if false
    // cond ? val1 : val2
    public static void main(String[] args){
    int a=5;
    int b=7;
    System.out.println(a>b?a:b);

    //absolute no. always positive
    int n= -2;
    System.out.println(n>0?n:n*(-1));

    }
}
